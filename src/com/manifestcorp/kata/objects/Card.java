package com.manifestcorp.kata.objects;

public class Card
{
	private int value;
	private int suit;


	public int getValue()
	{
		return value;
	}

	public void setValue(int value)
	{
		this.value = value;
	}

	public int getSuit()
	{
		return suit;
	}

	public void setSuit(int suit)
	{
		this.suit = suit;
	}


}
